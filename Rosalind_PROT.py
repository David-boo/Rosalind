# Split data by every 3 chars into an array. Compare that substring to the codons in your codon table until you find a match. Once matched, the corresponding protein letter can be added to a list or a new protein string.

with open ("rosalind_prot.txt", "r") as input_file:
    RNA = input_file.read()
    prot = ""
    for i in range(0, len(RNA), 3):
        if (codontable.get(RNA[i:i+3])) == "Stop":
            break
        else:
            prot += (codontable.get(RNA[i:i+3]))
print(prot)
